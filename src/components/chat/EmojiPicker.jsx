const EmojiPicker = ({ onSelect, onClose, position = 'bottom' }) => {
  const [activeCategory, setActiveCategory] = useState('Smileys');
  const [searchQuery, setSearchQuery] = useState('');

  const filteredEmojis = searchQuery
    ? Object.values(EMOJI_CATEGORIES).flat().filter(emoji => 
        emoji.toLowerCase().includes(searchQuery.toLowerCase())
      )
    : EMOJI_CATEGORIES[activeCategory];

  return (
    <div className="emoji-picker-overlay" onClick={onClose}>
      <div 
        className={`emoji-picker ${position}`} 
        onClick={(e) => e.stopPropagation()}
      >
        <div className="emoji-picker-header">
          <div className="emoji-search">
            <MdSearch />
            <input
              type="text"
              placeholder="Search emoji..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              autoFocus
            />
          </div>
          <button className="emoji-close-btn" onClick={onClose}>
            <MdClose />
          </button>
        </div>

        {!searchQuery && (
          <div className="emoji-categories">
            {Object.keys(EMOJI_CATEGORIES).map(category => (
              <button
                key={category}
                className={`emoji-category-btn ${activeCategory === category ? 'active' : ''}`}
                onClick={() => setActiveCategory(category)}
              >
                {category}
              </button>
            ))}
          </div>
        )}

        <div className="emoji-grid">
          {filteredEmojis.map((emoji, idx) => (
            <button
              key={idx}
              className="emoji-btn"
              onClick={() => {
                onSelect(emoji);
                onClose();
              }}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
};

export default EmojiPicker;
